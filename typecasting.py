'''Typecasting is a method in which one data type converts into another data type 
Typecasting can be of two types Explicit typecasting and Implicit typecasting
---> Explicit type : It is the type of typecasting in which data types changes to another data type 
via developers or programmers intervention
---> Implicit type : Data type changes according to the order of the data'''

#Explicit Typecasting
a = "1"
b = "2"
print(int(a)+int(b))

#Implicit Typecasting

c = 2
d = 3.5
print(c + d)